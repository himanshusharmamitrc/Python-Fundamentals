# use the pop() method to remove an item from the list.
fruits = ["apple", "banana", "cherry"]
fruits.pop()
print(fruits)

# use pop() to remove a number from the list.
numbers = [1, 2, 3, 4]      
numbers.pop()
print(numbers)

#use pop() to remove an item at a specific index from the list.
fruits = ["apple", "banana", "cherry"]
fruits.pop(1)
print(fruits)

#use pop() to remove a number at a specific index from the list.
numbers = [1, 2, 3, 4]  
numbers.pop(2)
print(numbers)
    